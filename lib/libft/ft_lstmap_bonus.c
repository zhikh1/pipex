#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*crnt;
	t_list	*new_lst;
	t_list	*tmp;

	crnt = lst;
	new_lst = NULL;
	while (crnt && f)
	{
		tmp = ft_lstnew(f(crnt->content));
		if (!tmp)
		{
			ft_lstclear(&new_lst, del);
			return (NULL);
		}
		ft_lstadd_back(&new_lst, tmp);
		crnt = crnt->next;
	}
	return (new_lst);
}
